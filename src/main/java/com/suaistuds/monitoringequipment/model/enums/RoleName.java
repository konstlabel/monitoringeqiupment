package com.suaistuds.monitoringequipment.model.enums;

/**
 * Перечисление ролей пользователей в системе.
 * <ul>
 *   <li>{@link #admin} — администратор, имеет полный доступ к функционалу.</li>
 *   <li>{@link #studio} — представитель студии, может управлять оборудованием и резервациями.</li>
 *   <li>{@link #user} — обычный пользователь, может просматривать и резервировать оборудование.</li>
 * </ul>
 */
public enum RoleName {
    /** Администратор системы. */
    admin,

    /** Представитель студии. */
    studio,

    /** Обычный пользователь. */
    user
}